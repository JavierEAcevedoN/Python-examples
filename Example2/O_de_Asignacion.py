# los operadores de asignacion son los que se utilizan para asignarle un valor a una variable estos son algunos tipos

# asignar
x = 3
print(x)

# asignar mas
x += 3
print(x)

# asignar menos
x -= 3
print(x)

# asignar multiplicado
x *= 3
print(x)

# asignar dividido
x /= 3
print(x)

# asignar potenciado
x **= 3
print(x)

# asignar cociente
x //= 3
print(x)

# asignar modulo
x %= 3
print(x)